    private void clientTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_clientTableMouseClicked
        // TODO add your handling code here:
            id = Integer.parseInt(model.getValueAt(clientTable.getSelectedRow(),0).toString());
          
            DefaultTableModel tblModel=(DefaultTableModel)clientTable.getModel();
            String tblNom = tblModel.getValueAt(clientTable.getSelectedRow(),1).toString();
            String tblPrenom = tblModel.getValueAt(clientTable.getSelectedRow(),2).toString();
            Date tblDate = (Date) (tblModel.getValueAt(clientTable.getSelectedRow(),3));
            //Service tb1Service = (Service)tblModel.getValueAt(clientTable.getSelectedRow(),4);
            String selectedServiceName = tblModel.getValueAt(clientTable.getSelectedRow(), 4).toString();
            
            txtNom.setText(tblNom);
            txtPrenom.setText(tblPrenom);
            txtDate.setDate(tblDate);
           
           // listService.setSelectedItem(tblService);
            for (Service service : ss.findAll()) {
                if (service.getNom().equals(selectedServiceName)) {
                    listService.setSelectedItem(service.getNom());
                 
                    
                           
                    break;
                }
            }
         
    
            
            
            
          
            
            
            
            
            
    }//GEN-LAST:event_clientTableMouseClicked


    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        id = Integer.parseInt(model.getValueAt(clientTable.getSelectedRow(),0).toString());
        DefaultTableModel tblModel=(DefaultTableModel)clientTable.getModel();
        String nom = txtNom.getText();
        String prenom = txtPrenom.getText();
        Date date = txtDate.getDate();
        String selectedServiceName = (String) listService.getSelectedItem();
        Service selectedService = null;
    for (Service service : ss.findAll()) {
        if (service.getNom().equals(selectedServiceName)) {
            selectedService = service;
            break;
        }
    }
        tblModel.setValueAt(nom,clientTable.getSelectedRow(),1);
        tblModel.setValueAt(prenom,clientTable.getSelectedRow(),2);
        tblModel.setValueAt(date,clientTable.getSelectedRow(),3);
        tblModel.setValueAt(selectedService.getNom(), clientTable.getSelectedRow(), 4);
        JOptionPane.showMessageDialog(this,"Updated Succesfully");
       cs.update(new Client(id ,nom, prenom, date,selectedService));
       load();
       
        
     
        
    }//GEN-LAST:event_updateButtonActionPerformed


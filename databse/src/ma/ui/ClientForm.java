    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
         int rep = JOptionPane.showConfirmDialog(this, "Voulez-vous supprimer ce client ?");
    if (rep == 0) {
        Client clientToDelete = cs.findById(id);
        if (clientToDelete != null) {
            if (cs.delete(clientToDelete)) {
                JOptionPane.showMessageDialog(this, "Supprimé");
                load();
            } else {
                JOptionPane.showMessageDialog(this, "Erreur lors de la suppression.", "Erreur", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Client introuvable.", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        String selectedServiceName = (String) listService.getSelectedItem();
Service selectedService = null;


for (Service service : ss.findAll()) {
    if (service.getNom().equals(selectedServiceName)) {
        selectedService = service;
        break;
    }
}
if (selectedService != null) {
    cs.create(new Client(txtNom.getText(), txtPrenom.getText(), txtDate.getDate(), selectedService));
    JOptionPane.showMessageDialog(this,"Succes");
} else {
    // Gérez le cas où aucun Service correspondant n'a été trouvé
    JOptionPane.showMessageDialog(this, "Aucun service correspondant trouvé.", "Erreur", JOptionPane.ERROR_MESSAGE);
}
load();

            
            
            
            
        
    }//GEN-LAST:event_addButtonActionPerformed

